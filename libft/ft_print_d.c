#include "ft_printf.h"

int	ft_print_d(int nb)
{
	int	i;

	i = 0;
	if (nb < 0)
	{
		if (nb == -2147483648)
		{
			write(1, "-2147483648", 11);
			return (11);
		}
		write(1, "-", 1);
		nb *= -1;
		i += 1;
	}
	if (nb < 10 && nb >= 0)
	{
		i += ft_print_c('0' + nb);
	}
	if (nb >= 10)
	{
		i += ft_print_d(nb / 10);
		i += ft_print_d(nb % 10);
	}
	return (i);
}
