#include <unistd.h>
#include "libft.h"

void	ft_putnbr_fd(int n, int fd)

{
	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		write(fd, "-", 1);
		n *= -1;
	}
	if (n < 10 && n >= 0)
		ft_putchar_fd(('0' + n), fd);
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
