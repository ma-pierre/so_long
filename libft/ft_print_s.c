#include "ft_printf.h"

int	ft_print_s(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (s[i] != '\0')
	{
		ft_print_c(s[i]);
		i++;
	}
	return (i);
}
